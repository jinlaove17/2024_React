// -> React, Next, Vue, Svelte들은 Node.js를 기반으로 한다.
//
// -> Node.js는 웹 브라우저가 아닌 환경에서도 자바스크립트 코드를 실행시켜주는
//    자바스크립트의 런타임, 즉 자바스크립트의 실행 환경(구동기)이다.
// -> Node.js는 Chrome V8 JavaScript 엔진으로 빌드된 JavaScript 런타임이다.
//
// -> 원래 JavaScript는 웹 페이지 내부에 필요한 아주 단순한 기능만을 개발하기 위해 만들어졌다.
//    따라서, 매우 유연하고 작성하기 편리하도록 설계 되어 생산성이 높았다.
// -> Node.js가 등장함에따라 JavaScript는 웹 브라우저 이외에서도 개발이 가능해졌다.
//    ex) 웹 서버, 모바일 앱, 데스크톱 앱
